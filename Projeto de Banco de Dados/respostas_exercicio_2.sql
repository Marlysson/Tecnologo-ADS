CREATE TABLE FUNCIONARIO (
	COD_FUNC INT NOT NULL PRIMARY KEY,
	NOME VARCHAR NOT NULL,
	DT_NASC DATE,
	SALARIO FLOAT NOT NULL
)

-- ADICIONANDO COLUNA E CONSTRAINT DE CHAVE ESTRANGEIRA PARA HOSPEDAGEM - FUNCIONARIO

