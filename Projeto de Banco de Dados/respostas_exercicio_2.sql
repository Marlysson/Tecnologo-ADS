SELECT NOME_CAT , COALESCE(CAST(NUM AS VARCHAR),'Não possui apartamento') as "Apartamento"
	FROM CATEGORIA LEFT OUTER JOIN APARTAMENTO
	ON APARTAMENTO.COD_CAT = CATEGORIA.COD_CAT
	ORDER BY NOME_CAT ASC , NUM ASC


