(SELECT COD_FUNC FROM HOSPEDAGEM
	NATURAL JOIN FUNCIONARIO
	NATURAL JOIN APARTAMENTO
	NATURAL JOIN CATEGORIA
	WHERE COD_HOSP IN (SELECT COD_HOSP FROM HOSPEDE WHERE UPPER(NOME) LIKE 'MARLYSSON%')
	OR UPPER(NOME_CAT) LIKE 'EXECUTIVO'
UNION
SELECT COD_FUNC FROM RESERVA
	NATURAL JOIN FUNCIONARIO
	NATURAL JOIN APARTAMENTO
	NATURAL JOIN CATEGORIA
	WHERE COD_HOSP IN (SELECT COD_HOSP FROM HOSPEDE WHERE UPPER(NOME) LIKE 'MARLYSSON%')
	OR UPPER(NOME_CAT) LIKE 'EXECUTIVO') ORDER BY NOME ASC

